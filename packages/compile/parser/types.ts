export const enum CharCodes {
  LEFT_CURLY_BRACE = 0x7B,    // {
  RIGHT_CURLY_BRACE = 0x7D,   // }
  SINGLE_QUOTE = 0x27,        // '
  DOUBLE_QUOTE = 0x22,        // "
  BACK_QUOTE = 0x60,          // `
  BACK_SLASH = 0x5C,          // \
  SLASH = 0x2F,               // /
  LESS_THAN = 0x3C,           // <
  GREATER_THAN = 0x3E,        // >
  MINUS = 0x2D,               // -
  UNDER_LINE = 0x5F,          // _
  EQUALS = 0x3D,              // =
  EXCLAMATION = 0x21,         // !
  COLON = 0x3A,               // :
  LEFT_PAREN = 0x28,          // (
  RIGHT_PAREN = 0x29,         // (
  DOT = 0x2E,                 // .

  TAB = 0x09,                 // \t
  LINE_FEED = 0x0A,           // \n
  CARRIAGE_RETURN = 0x0D,     // \r
  SPACE = 0x0020,             // " "

  UPPER_A = 0x41,             // A
  UPPER_Z = 0x5A,             // Z
  LOWER_A = 0x61,             // a
  LOWER_Z = 0x7A,             // a

  _0 = 0x30,
  _9 = 0x39,
  $ = 0x24,
}

export const WXS_LITERAL = 'wxs'
